let result = [];

function add(x,y){
    let z = x + y;
    result.push(z);
}

function substract(x,y){
    let z = x - y;
    result.push(z);
}

function multiply(x,y){
    let z = x * y;
    result.push(z);
}

function divide(x,y){
    let z = x / y;
    result.push(z);
}

